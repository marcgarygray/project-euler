class SmallestMultiple {
    constructor(max) {
        this.max = max;
    }

    get value() {
        let match = false;
        let value = this.max;
        while (!match) {
            value ++;
            let counter = this.max;
            while (counter > 0) {
                match = true;
                if (value % counter !== 0) {
                    match = false;
                    break;
                }
                counter--;
            }
        }
        return value;
    }
}
const smallestMultiple = new SmallestMultiple(20);
console.log(smallestMultiple.value); // output: 232792560